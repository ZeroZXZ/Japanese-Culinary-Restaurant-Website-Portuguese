		<h class="titulo">Historia do Sushi</h>
		<p class="texto"><img src="imagens/originais/sushi.jpg" class="imagem_direita" id="img_hover" alt="sushi roll">O sushi tornou-se popular quando os pescadores japoneses perceberam que se poderia embrulhar peixe e outras carnes dentro do arroz  pegajoso para fazer com que a fermentação torna-se mais rápida. Essa fermentação previne que as bactérias prejudiciais cresçam em seu interior. O sushi tem uma origem e cultura muito interessantes que já duram há séculos e que continua a ser uma fonte de alimento muito popular até hoje. O sushi é feito com uma combinação de frutos do mar, cozidos ou peixes crus, legumes e arroz temperado. Muito embora o sushi seja mais comumente associada à herança japonesa, na verdade, começou na China durante o século 7. </p>
		<p class="texto">Naquela época, todos os peixes capturados tinham de ser preservados, e o único método possível era através da fermentação. O peixe cru era limpo, fatiado em filetes, e em seguida, pressionado entre camadas de sal pesado e geralmente sobrecarregados com algum tipo de pedra. Os peixes permaneciam assim por semanas, então a pedra era removida e então substituída por algum tipo de cobertura mais leve. Porém, o peixe ficaria nas camadas de sal por alguns meses até que o processo de fermentação ficasse completo.</p>
		<p class="texto">Com o tempo descobriram que, enrolando o peixe no arroz que tinha sido embebido em vinagre o peixe foi fermentado em uma questão de dias, em vez de meses. O arroz foi então jogado fora e os peixes eram consumidos. No entanto, com a seca e a escassez de alimentos, as pessoas começaram a consumir o arroz, bem como o peixe e, portanto, o sushi tal como o conhecemos hoje nasceu.</p>
		<p class="texto"><img src="imagens/originais/temaki.jpg" class="imagem_esquerda" id="img_hover" alt="temaki">No entanto, em 1800, um chef muito famoso pelo nome de Yohei estava planejando uma grande festa. Foi constatado de que ele não tinha preparado peixe suficiente para servir seus convidados, então ele pegou um pedaço de peixe do congelador que não tinha sido fermentado e decidiu experimentar servi-lo. O que ele descobriu foi que o peixe congelado realmente manteve seu sabor e que qualquer bactéria era morta. A partir desta descoberta, Yohei criou dois estilos de Sushi - um chamado Edo, que começou como "Edomaezushi" cuja tradução significa "na frente de Edo" fazendo referência a captura de peixes em frente à cidade de Edo, e a segunda chamada Osaka, em referência a cidade de Osaka.</p>
		<p class="texto">Curiosamente, os comerciantes da cidade de Osaka foram conhecidos por ainda fazer um tipo diferente de sushi que consistia em arroz temperado misturado com outros ingredientes e em seguida, transformado em uma variedade de "pacotes" de decoração que as pessoas poderiam comer. Em Tóquio, no Japão, o mar estava carregado com mariscos e peixes ricos. Foi a partir desta cidade que o niguri foi criado, que consistia em tomar um pequeno pedaço do peixe e servi-lo em uma almofada de arroz temperado. Na verdade, se você fosse visitar o Japão hoje, seria mais provável ser servido o estilo de sushi niguri.</p>
		<p class="texto">O sushi tomou o mundo por uma tempestade e hoje é uma indústria multi-bilionária. Desde 1970, mais de 5.000 restaurantes de sushi foram abertas somente nos Estados Unidos e há apenas alguns anos atrás, uma venda anual de algas chegou a US$ 36 milhões de doláres. A popularidade do Sushi continua a aumentar na medida em que as pessoas estão à procura de comida saudável, que é rápido e fácil de fazer. </p>
		<p class="texto">De acordo com o Recorde Mundial dos Guinnes, existem dois registros de sushi. A primeira foi criada em janeiro em 1992, em que um atum de barbatana de 324kg foi vendido por mais de US$83.000 e usado para criar 2.400 porções de sushis para jantares influentes em um escalonamento de $75 por dose. O segundo registro consistiu na criação do mais longo rolo de sushi do mundo. Levou mais de 600 pessoas para criar um roll de pepino com aproximadamente 1000 metros, em outubro de 1997.</p>
		<p class="texto">Com incrível tamanha popularidade, bem como os benefícios a saúde, é provável que este alimento irá continuar a ser uma parte importante na vida cotidiana das pessoas por muito mais anos</p>
